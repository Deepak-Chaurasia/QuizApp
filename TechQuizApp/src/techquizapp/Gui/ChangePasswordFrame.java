    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int response = validateInputs();
        if (response == 0) {
            JOptionPane.showMessageDialog(null, "Password cannot be left blank!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (response == -1) {
            JOptionPane.showMessageDialog(null, "Password should be more than 4 characters long!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (response == -2) {
            JOptionPane.showMessageDialog(null, "Password and retypePassword do not match!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            tv.changeState();
            int status = UserDao.changePassword(password, userid);
            if (status != 0) {
                JOptionPane.showMessageDialog(null, "Password Changed!!", "UserId: " + userid + "\nPassword: " + password, JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            } else {
                JOptionPane.showMessageDialog(null, "Cannot change Password!", "Error!", JOptionPane.ERROR_MESSAGE);
            }
            vt.stop();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error while connecting to DB!", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

