    private int validateInputs() {
        char[] pwd = txtPassword.getPassword();
        char[] repwd = txtReType.getPassword();

        if (pwd.length == 0 || repwd.length == 0) {
            return 0;
        } else {
            if (pwd.length < 4) {
                return -1;
            }
            if (!((String.valueOf(pwd)).equals(String.valueOf(repwd)))) {
                return -2;
            } else {
                password = String.valueOf(pwd);
                return 1;
            }
        }
    }

    private void clearAll() {
        txtPassword.setText("");
        txtReType.setText("");
    }

    class TextToVoice extends Thread {
        public static final String VOICENAME = "kevin16";
        private Voice voice;
        public String speech = "";

        public void run() {
            try {
                Thread.sleep(700);
                //	Voice voice;
                VoiceManager vm = VoiceManager.getInstance();
                voice = vm.getVoice(VOICENAME);
                voice.allocate();
                voice.speak(speech);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        public void changeState() {
            voice.deallocate();
        }
    }

    class VoiceToText extends Thread {
        private boolean flag = true;

        public void run() {
            try {
                Thread.sleep(500);
                while (flag) {
                    String speech = LoginSR.speech();
                    System.out.println("speech: Change password " + speech);
                    if (speech.equalsIgnoreCase("<unk>")); else if (speech.equalsIgnoreCase("logout")) {
                        disposeMethod();
                    } else if (speech.equalsIgnoreCase("Do task")) {
                        btnUpdate.doClick();
                    } else if (speech.equalsIgnoreCase("back")) {
                        btnBack.doClick();
                    } else;
                }
                //recognizer.stopRecognition();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    public void disposeMethod() {
        this.dispose();
        LoginFrame loginFrame = new LoginFrame();
        loginFrame.setVisible(true);
        vt.stop();
    }
