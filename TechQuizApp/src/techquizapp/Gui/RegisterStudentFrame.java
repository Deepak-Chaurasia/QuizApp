    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        tv.changeState();
        int response = validateInputs();
        if(response == 0){
            JOptionPane.showMessageDialog(null,"Username or Password cannot be left blank!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(response == -1){
            JOptionPane.showMessageDialog(null,"Username or Password should be more than 4 characters long!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(response == -2){
            JOptionPane.showMessageDialog(null,"Password and retypePassword do not match!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            UserPojo user = new UserPojo();
            user.setUserid(userid);
            user.setUserType("student");
            user.setPassword(password);
            boolean status = UserDao.addUser(user);
            
            if(status){
                JOptionPane.showMessageDialog(null,"Registration Done!!", "UserId:"+userid+"\nPassword:"+password, JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }
            else
                JOptionPane.showMessageDialog(null,"UserId already exists", "Error!", JOptionPane.ERROR_MESSAGE);
            vt.stop();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error while connecting to DB!", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

