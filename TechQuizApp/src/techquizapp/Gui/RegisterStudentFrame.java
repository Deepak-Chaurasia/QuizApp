    class TextToVoice extends Thread{
        public static final String VOICENAME = "kevin16";
        private Voice voice;
        public void run(){
            try{
                Thread.sleep(700);
                VoiceManager vm = VoiceManager.getInstance();
                voice = vm.getVoice(VOICENAME);
                voice.allocate();
                voice.speak("Register new student. Enter user i d and password");
            }
            catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        public void changeState() {
        	voice.deallocate();
        }
    }
        
    class VoiceToText extends Thread {
        private boolean flag = true;
        public void run() {
            try {
                Thread.sleep(500);
                while (flag) {
                    String speech = LoginSR.speech();
                    System.out.println(speech);
                    if (speech.equalsIgnoreCase("<unk>"));
                    else if (speech.equalsIgnoreCase("logout")) {
                        disposeMethod();
                    } else if (speech.equalsIgnoreCase("Do task")) {
                        btnRegister.doClick();
                    } else  if(speech.equalsIgnoreCase("back"))
    			btnBack.doClick();
                    else;
                }
                //recognizer.stopRecognition();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

