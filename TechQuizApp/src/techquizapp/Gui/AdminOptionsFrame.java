	private void btnDoTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoTaskActionPerformed
            tv.changeState();
            if (jrbSetPaper.isSelected()) {
                SetPaperFrame setPaper = new SetPaperFrame();
                setPaper.setVisible(true);
                this.dispose();
            } else if (jrbEditPaper.isSelected()) {
                EditPaperFrame setPaper = new EditPaperFrame();
                setPaper.setVisible(true);
                this.dispose();
            } else if (jrbRegister.isSelected()) {
                RegisterStudentFrame setPaper = new RegisterStudentFrame();
                setPaper.setVisible(true);
                this.dispose();
            } else if (jrbViewScore.isSelected()) {
                ViewScoreFrame setPaper = new ViewScoreFrame();
                setPaper.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Please select a correct option");
                return;    //because of below this.dispose();
            }
            this.dispose();
            vt.stop();
    }//GEN-LAST:event_btnDoTaskActionPerformed

